package pub.test;

public class ChildMenu {
	private String name;
	private String url;
	
	public ChildMenu(){
		name = "";
		url = "";
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}

}
